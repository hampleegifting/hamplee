#!/usr/bin/env node
/**
 * Hamplee — Build Script
 * ──────────────────────
 * Converts data/hampers.json  →  data/hampers-data.js
 *
 * Run after editing hampers.json:
 *   node scripts/build-hampers-data.js
 *
 * The website reads from hampers-data.js (works both locally
 * and on Vercel without needing a web server for fetch()).
 */

const fs   = require('fs');
const path = require('path');

const ROOT     = path.join(__dirname, '..');
const jsonPath = path.join(ROOT, 'data', 'hampers.json');
const jsPath   = path.join(ROOT, 'data', 'hampers-data.js');

try {
  const raw  = fs.readFileSync(jsonPath, 'utf8');
  const data = JSON.parse(raw);

  const output = [
    '// AUTO-GENERATED — do not edit this file directly.',
    '// Edit data/hampers.json, then run: node scripts/build-hampers-data.js',
    '//',
    `// Last built: ${new Date().toISOString()}`,
    `// Hampers: ${data.hampers.length}`,
    '',
    'window.HAMPLEE_DATA = ' + JSON.stringify(data, null, 2) + ';',
    ''
  ].join('\n');

  fs.writeFileSync(jsPath, output, 'utf8');

  console.log(`✓ Built data/hampers-data.js`);
  console.log(`  ${data.hampers.length} hampers across ${[...new Set(data.hampers.map(h => h.category))].length} categories`);
  console.log(`  ${data.hampers.filter(h => h.featured).length} featured on homepage`);
} catch (err) {
  console.error('✗ Build failed:', err.message);
  process.exit(1);
}
